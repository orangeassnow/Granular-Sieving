function value= Ursem01(x)
%----------------------------------------------------------
% Ursem01 Function for Nonlinear Optimization
%
% -2.5 <= x1 <= 3         
% -2 <= x2 <= 2   
% fmin = -4.8168;
% xmin = [1.69714,0]
%----------------------------------------------------------

x1 = x(:,1);
x2 = x(:,2);
value = -sin(2.*x1-0.5.*pi)-3.*cos(x2)-0.5.*x1;