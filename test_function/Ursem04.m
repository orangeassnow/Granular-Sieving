function value = Ursem04(x)
%----------------------------------------------------------
% Ursem04 Function for Nonlinear Optimization
%
% -2 <= x1,x2 <= 2               
% fmin = -1.5;
% xmin = [-0,0]; 
%----------------------------------------------------------

x1 = x(:,1);
x2 = x(:,2);
a = 2-sqrt(x1.^2+x2.^2);
value = -0.75.*a.*sin(0.5.*pi.*x1+0.5.*pi);