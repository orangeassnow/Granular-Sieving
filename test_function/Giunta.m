function value = Giunta(x)
%----------------------------------------------------------
% Giunta Function for Nonlinear Optimization
%
% -1 <= x1,x2<= 1 
% fmin = 0.06447042053690566;
% xmin = [0.4673200277395354,0.4673200169591304];
%----------------------------------------------------------

a = (16/15).*x-1;
b = sin(a);
value = 0.6+sum(b+b.^2+0.02.*sin(4.*a),2);
end
