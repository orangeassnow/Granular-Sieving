function value = McCormick(x)
%----------------------------------------------------------
% McCormick Function for Nonlinear Optimization
%
% -1.5 <= x1 <= 4             
% -3 <= x2 <= 4
% fmin = -1.913222954981037;
% xmin = [-0.5471975602214493,-1.547197559268372];
%----------------------------------------------------------

x1 = x(:,1);
x2 = x(:,2);
value = sin(x1+x2)+(x1-x2).^2-1.5.*x1+2.5.*x2+1;
