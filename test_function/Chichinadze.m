function value = Chichinadze(x)
%----------------------------------------------------------
% Chichinadze Function for Nonlinear Optimization
%
% -30 <= x <= 30        
% fmin = -42.94438701899098;
% xmin = [6.189866586965680,0.5];
%----------------------------------------------------------

x1 = x(:,1);
x2 = x(:,2);
a = pi.*x1./2;
b = 0.2.^0.5.*exp(-0.5.*(x2-0.5).^2);
value = x1.^2-12.*x1+11+10.*cos(a)+8.*sin(5.*a)-b;