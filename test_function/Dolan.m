function value = Dolan(x)
%----------------------------------------------------------
% Dolan Function for Nonlinear Optimization
%
% -100 <= x1,x2,x3,x4,x5 <= 100      
% fmin = 0;
% xmin = 
%
%----------------------------------------------------------

x1 = x(:,1);
x2 = x(:,2);
x3 = x(:,3);
x4 = x(:,4);
x5 = x(:,5);
value = (x1+1.7.*x2).*sin(x1)-1.5.*x3-0.1.*x4.*cos(x4+x5-x1)+0.2.*(x5.^2)-x2-1;
end
