function value = Bukin02(x)
%----------------------------------------------------------
% Bukin02 Function for Nonlinear Optimization
%
% -15 <= x1 <= -5             
% -3 <= x2 <= 3
% fmin = ;
% xmin = ;
%----------------------------------------------------------

x1 = x(:,1);
x2 = x(:,2);
value = 100.*(x2-0.01.*x1.^2+1)+0.01.*(x1+10).^2;
end