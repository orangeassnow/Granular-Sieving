function value = Zirilli(x)
%----------------------------------------------------------
% Zirilli Function for Nonlinear Optimization
%
% -10 <= x1,x2 <= 10 
% fmin = -0.3523;
% xmin = [-1.0465,0]; 
%----------------------------------------------------------

x1 = x(:,1);
x2 = x(:,2);
b = x2.^2;
value = x1.*(0.1+x1.*(-0.5+x1.^2.*0.25))+0.5.*b;