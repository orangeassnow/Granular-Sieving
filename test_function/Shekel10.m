function value = Shekel10(x)
%----------------------------------------------------------
% Shekel10 Function for Nonlinear Optimization
%
% 0 <= x1 <= 10                 
% 0 <= x2 <= 10
% 0 <= x3 <= 10
% 0 <= x4 <= 10
% fmin = ;
% xmin = ;
%----------------------------------------------------------

a = [4,4,4,4;
     1,1,1,1;
     8,8,8,8;
     6,6,6,6;
     3,7,3,7;
     2,9,2,9;
     5,5,3,3;
     8,1,8,1;
     6,2,6,2;
     7,3.6,7,3.6];
c = [ 0.1, 0.2, 0.2, 0.4, 0.4, 0.6, 0.3, 0.7, 0.5, 0.5];
[m,n] = size(x);
d = zeros(m,n);
for i=1:10
    b = (x - repmat(a(i,:),m,1)).^2;
    d(:,i) = sum(b,2);
end
value = -sum((repmat(c,m,1)+d).^(-1),2);