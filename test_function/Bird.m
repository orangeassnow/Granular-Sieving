function value = Bird(x)
%----------------------------------------------------------
% Bird Function for Nonlinear Optimization
%
% -2pi <= x1 <= 2pi
% -2pi <= x2 <= 2pi     
% fmin = -106.764537;
% xmin = [4.70104,3.15294],[-1.58214,-3.13024];
%----------------------------------------------------------

x1 = x(:,1);
x2 = x(:,2);
a = sin(x1).*exp((1-cos(x2)).^2);
b = cos(x2).*exp((1-sin(x1)).^2);
value = a+b+(x1-x2).^2;