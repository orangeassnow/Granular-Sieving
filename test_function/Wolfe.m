function value= Wolfe(x)
%----------------------------------------------------------
% Wolfe Function for Nonlinear Optimization
%
% 0 <= x1,x2,x3 <= 2             
% fmin = 0;
% xmin = [0,0,0];
%----------------------------------------------------------

x1 = x(:,1);
x2 = x(:,2);
x3 = x(:,3);
value = 4/3.*(x1.^2+x2.^2-x1.*x2).^0.75+x3;
end