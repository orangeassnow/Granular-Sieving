function value = Leon(x)
%----------------------------------------------------------
% Leon Function for Nonlinear Optimization
%
% -1.2 <= x1,x2<= 1.2
% fmin = 0;
% xmin = [1,1];
%----------------------------------------------------------

x1 = x(:,1);
x2 = x(:,2);
a = x2-x1.^2;
b = (1-x1).^2;
value = 100.*a.^2+b;
end
