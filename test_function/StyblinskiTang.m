function value = StyblinskiTang(x)
%----------------------------------------------------------
% StyblinskiTang Function for Nonlinear Optimization
%
% -5 <= x <= 5               
% fmin = -39.16616570377142*n;
% xmin = [-2.903534018185960,...,-2.903534018185960];
%----------------------------------------------------------

value = 0.5.*sum(x.*(5+x.*(-16+x.^2)),2);