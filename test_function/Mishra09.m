function value = Mishra09(x)
%----------------------------------------------------------
% Mishra09 Function for Nonlinear Optimization
%
% -10 <= x <= 10
% fmin = 0;
% xmin = [1,2,3];
%----------------------------------------------------------

x1 = x(:,1);
x2 = x(:,2);
x3 = x(:,3);
a = x1.*(2*x1.^2+5.*x2-2.*x1.*x3)+4.*x3-18;
b = x1.*(1+x2.^2+x3.^2)+x2.^3-22;
c = 8.*x1.^2+x2.*(2.*x3+2.*x2+3.*x2.^2)-52;
d = (x1+x2-x3).^2;
value =(b.*(a.*c.*(b+c)+b)+d).^2;
end