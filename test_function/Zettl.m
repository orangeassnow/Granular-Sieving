function value = Zettl(x)
%----------------------------------------------------------
% Zettl Function for Nonlinear Optimization
%
% -1 <= x1,x2 <= 5 
% fmin = -0.0037912;
% xmin = [-0.029896,0]; 
%----------------------------------------------------------

x1 = x(:,1);
x2 = x(:,2);
a = x1.^2;
b = x2.^2;
value = (a+b-2.*x1).^2+0.25.*x1;