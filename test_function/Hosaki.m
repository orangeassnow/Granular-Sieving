function value = Hosaki(x)
%----------------------------------------------------------
% Hosaki Function for Nonlinear Optimization
%
% 0 <= x1 <= 10
% 0 <= x2 <= 10        
% fmin = -2.3458;
% xmin = [4,2];
%----------------------------------------------------------

x1 = x(:,1);
x2 = x(:,2);
a = x1.*(-8+x1.*(7+x1.*(-7/3+x1.*0.25)));
b = x2.^2.*exp(-x2);
value = (1+a).*b;
end