function value = Bukin04(x)
%----------------------------------------------------------
% Bukin04 Function for Nonlinear Optimization
%
% -15 <= x1 <= -5             
% -3 <= x2 <= 3
% fmin = 0;
% xmin = [-10,0];
%----------------------------------------------------------

x1 = x(:,1);
x2 = x(:,2);
value = 100.*(x2).^2 + 0.01.*abs(x1+10);
end