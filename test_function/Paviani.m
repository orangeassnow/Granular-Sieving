function value = Paviani(x)
%----------------------------------------------------------
% Paviani Function for Nonlinear Optimization
%
% 2.001 <= x <= 9.999           
% fmin = -45.7784684040686;
% xmin = [9.350266,9.350266,9.350266,9.350266,9.350266,9.350266,9.350266,9.350266,9.350266,9.350266];
%----------------------------------------------------------

a = (log(x-2)).^2;
b = (log(10-x)).^2;
value = sum(a+b,2)-(prod(x,2)).^0.2;