function value = Beale(x)
%----------------------------------------------------------
% Beale Function for Nonlinear Optimization
%
% -10 <= x1 <= 10                
% -10 <= x2 <= 10
% fmin = 0;
% xmin = [3,0.5]; 
%----------------------------------------------------------

x1 = x(:,1);
x2 = x(:,2);
value = (1.5-x1+x1.*x2).^2+(2.25-x1+x1.*x2.^2).^2+(2.625-x1+x1.*x2.^3).^2;
