function value = SixHumpCamel(x)
%--------------------------------------------------------
% SixHumpCamel Function for Nonlinear Optimization
%
% -5 <= x1 <= 5                  
% -5 <= x2 <= 5                 
% fmin = -1.031628453489877;
% xmin = [0.08984201368301331,-0.7126564032704135;-0.08984201368301331,0.7126564032704135];
%--------------------------------------------------------
      
x1 = x(:,1);
x2 = x(:,2);
value =(4-2.1.*x1.^2+x1.^4./3).*x1.^2+x1.*x2+(-4+4.*x2.^2).*x2.^2;    

