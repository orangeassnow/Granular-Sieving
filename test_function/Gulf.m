function value = Gulf(x)
%----------------------------------------------------------
% Gulf Function for Nonlinear Optimization
%
% [0.1,100;0,25.6;0,5]
% fmin = 0;
% xmin = [50,25,2.5];
%----------------------------------------------------------

x1 = x(:,1);
x2 = x(:,2);
x3 = x(:,3);
value = 0;
for i =1:99
    b = 25+(-50.*log(0.01.*i))^(2/3);
    a = exp(-((b-x2).^x3)./x1);
    value =value + (a-0.01.*i).^2;
end
end
