function value = Cube(x)
%----------------------------------------------------------
% Cube Function for Nonlinear Optimization
%
% -10 <= x1,x2 <= 10           
% fmin = 0;
% xmin = [1,1];
%----------------------------------------------------------

x1 = x(:,1);
x2 = x(:,2);
value = 100.*(x2-x1.^3).^2 + (1-x1).^2;
end